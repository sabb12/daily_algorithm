function solution(my_string) {
    
    let result = "";
    for(let i = 0; i < my_string.length; i++){
        if(my_string[i] === "a"){
            result +=""
        }else if(my_string[i] === "e"){
             result += ""
        }else if(my_string[i] === "i"){
             result += ""
        }else if(my_string[i] === "o"){
             result += ""
        }else if(my_string[i] === "u"){
            result += ""
        }else{
            result += my_string[i]
        }
    }
    
    return result;
}